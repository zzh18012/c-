// ============================================================
// CollisionSystem.h - 碰撞检测系统声明
// ============================================================
// 职责：提供游戏中的碰撞检测功能
// 目前只实现了圆形碰撞检测（用于子弹vs玩家、Boss等）
// ============================================================

#pragma once

// SFML图形库（用于sf::Vector2f）
#include <SFML/Graphics.hpp>

// ============================================================
// CollisionSystem类
// ============================================================
// 静态工具类，提供碰撞检测函数
// 所有方法都是static，可直接通过类名调用
// 碰撞检测基于圆形碰撞盒（circle-circle）
class CollisionSystem {
public:
    // ============================================================
    // checkCircleCollision - 圆形碰撞检测
    // ============================================================
    // pos1: 第一个物体的位置
    // r1: 第一个物体的碰撞半径
    // pos2: 第二个物体的位置
    // r2: 第二个物体的碰撞半径
    // 返回：true=碰撞，false=未碰撞
    // 原理：比较两圆心距离与半径之和
    // 优化：比较距离的平方避免开方运算
    static bool checkCircleCollision(
        const sf::Vector2f& pos1, float r1,
        const sf::Vector2f& pos2, float r2
    );
};